/* USER CODE END Header_StartDefaultTask */
void StartDefaultTask(void const * argument)
{
  /* USER CODE BEGIN 5 */
	
	phy.pfn = DMA_IrqHandler;
  vSemaphoreCreateBinary( phy.xSemaphore  );

	init_idleDedection();
	
	xSemaphoreTake( phy.xSemaphore , 0);

  /* Infinite loop */
  for(;;)
  {
		xSemaphoreTake( phy.xSemaphore, portMAX_DELAY );
		
		sprintf(Buffer,"%d\r\n",phy.dataLen);
		HAL_UART_Transmit(&huart1,Buffer,strlen(Buffer),10);
		HAL_GPIO_TogglePin(GPIOG, GPIO_PIN_13|GPIO_PIN_14);
		
  }
  /* USER CODE END 5 */ 
}

